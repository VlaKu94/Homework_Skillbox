/*
Создайте упрощённую модель механического пианино. Всего у данного пианино семь клавиш, которые соответствуют семи нотам.
Каждая клавиша кодируется уникальной битовой маской, которая записывается в enum.
В начале программы пользователь вводит мелодию. Ввод осуществляется с помощью цифр на клавиатуре,
от 1 до 7 включительно (от ноты до до ноты си). В результате из чисел может быть составлена любая комбинация нот.
К примеру 512 или 154. После ввода комбинации вводится следующая. Всего таких комбинаций нот — 12.
Как только все комбинации были введены пользователем, мелодия проигрывается.
Для этого каждая комбинация нот последовательно выводится на экран. Однако на этот раз она печатается «красиво»,
то есть все ноты указываются словами, а не цифрами. При этом слова разделяются пробелами.
*/

#include <iostream>
#include <string>
#include <vector>

enum note
{
    DO = 1,
    RE = 2,
    MI = 4,
    FA = 8,
    SOL = 16,
    LA = 32,
    SI = 64
};

int main() {
    std::vector<int> notes;
    std::string melody;
    while(notes.size() < 12) {
        int note;
        std::cout << "Input note(1-7):";
        std::cin >> note;
        notes.push_back(note);
    }

    for(const auto& note : notes) {
        int accord = 1 << note;
        if (accord & DO){
            melody += "DO ";
        }
        if (accord & RE){
            melody += "RE ";
        }
        if (accord & MI){
            melody += "MI ";
        }
        if (accord & FA){
            melody += "FA ";
        }
        if (accord & SOL){
            melody += "SOL ";
        }
        if (accord & LA){
            melody += "LA ";
        }
        if (accord & SI){
            melody += "SI ";
        }
    }
    std::cout << melody << std::endl;

}










